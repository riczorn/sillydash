    public function __construct()
    {
        parent::__construct();

        // Ensure that we always set the database group to 'tests' if
        // we are currently running an automated test suite, so that
        // we don't overwrite live data on accident.
        if (ENVIRONMENT === 'testing') {
            $this->defaultGroup = 'tests';
        }

        // Ensure sensible defaults for environment
        $this->default['DBDebug'] = (ENVIRONMENT !== 'production');

        // Load config.php if it exists
        if (file_exists(ROOTPATH . 'config.php')) {
            $customConfig = require ROOTPATH . 'config.php';
            if (isset($customConfig['database']) && is_array($customConfig['database'])) {
                $this->default = array_merge($this->default, $customConfig['database']);
            }
            // Ensure db_prefix is respected if set at top level and not in database array, 
            // though ideally it should be in the database array.
            if (isset($customConfig['db_prefix']) && empty($customConfig['database']['DBPrefix'])) {
                $this->default['DBPrefix'] = $customConfig['db_prefix'];
            }
        }
    }
